function display_energy_info(energy_info_list) {
    const container = document.getElementById('energy_container');
    
    energy_info_list.forEach(energy => {
        const energyDiv = document.createElement('div');
        energyDiv.className = 'types-of-energy';
        energyDiv.innerHTML = `
            <div class="energy-content" onclick="open_info_page(${energy.id})">
                <h3 class="energy-title">${energy.title}</h3>
                <img src="${energy.image_url}" alt="${energy.title}" class="energy-image">
                <p id="desc-${energy.id}" class="energy-description">${energy.description}</p>
            </div>
            <div class="button-container">
                <button onclick="speakText('desc-${energy.id}')">Listen</button>
                <button onclick="stopSpeaking()">Stop</button>
            </div>
        `;

        container.appendChild(energyDiv);
    });
}
